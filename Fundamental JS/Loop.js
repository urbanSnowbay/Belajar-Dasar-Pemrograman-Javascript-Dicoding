// for
for(i = 0; i <= 10; i++) {
    console.log(i);
}

// for of
const myArray = ["Hanal", "Tul", "Set"]
for(const arrayItem of myArray) {
    console.log(arrayItem)
}

// While
let h = 1;
while (h <= 5) {
    console.log(h);
    h++;
}
// while lebih cocok digunakan pada kasus di mana kita tidak tahu pasti berapa banyak perulangan yang diperlukan.

// do-while
let x = 1;
do {
    console.log(x);
    x++
} while (x <= 5);
